/*
	========== Belajar Tipe Data Tuple ==========
 Tipe Data Tuple adalah jenis tipe
 data yang mirip seperti array, namun
 dia itu memiliki nilai yang terbatas.
*/
const ryan: [string, number] = ['Node Js', 2009];
console.log(ryan);
