// Belajar Tipe Data Array
/*
	====== Belajar Tipe Data Aray ======
 rumus :
 let namaVariable: tipeData[] = value;
 let namaVariable: tipeData[];
*/
let framework: string[] = ['Express Js', 'Sails Js', 'Adonis Js'];
console.log(framework);

let nilai: number[] = [1,2,3,4,5];
nilai = [1,2,3,4,5];
